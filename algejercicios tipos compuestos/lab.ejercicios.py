
#Concatenar Cadenas
mensaje1 = 'Hola' + ' ' + 'Mundo'
print(mensaje1)

#Multiplicar Cadenas
mensaje2a = 'Hola ' * 3
mensaje2b = 'Mundo'
print(mensaje2a + mensaje2b)

#AÃ±adir Cadenas
mensaje3 = 'Hola'
